class Exercise {
  final String id;
  final String name;
  final String description;
  final String imageUrl;
  final int sets;
  final int reps;
  final int restTime;

  Exercise({
    required this.id,
    required this.name,
    required this.description,
    required this.imageUrl,
    required this.sets,
    required this.reps,
    required this.restTime,
  });
}